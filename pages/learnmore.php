        <p class="health"><b>Cancer</b>
        </br>Cancer is a complex and varied group of diseases characterized by the uncontrolled growth and spread of abnormal cells. These abnormal cells can form tumors and invade nearby tissues and organs, and can also spread to other parts of the body through the bloodstream or lymphatic system. </br></p>

