        </br>The experience of being mistreated, neglected, or harmed by another person, which can lead to psychological, emotional, and physical trauma.</br></br>
        <b>Acquired Immune Deficiency Syndrome (AIDS)</b>
        </br>A viral infection that attacks the immune system, which can lead to a range of physical and mental health complications.</br></br>
        <b>Addictions (including Drugs, Alcohol and Gambling)</b>
        </br> A condition characterized by compulsive use of a substance or behavior despite negative consequences, which can impact physical, emotional, and social well-being.</br></br>
        <b>Anger</b>
        </br>A normal emotion that can become problematic when expressed in unhealthy or violent ways, which can impact social and emotional functioning.</br></br>
        <b>Anorexia</b> 
        </br>An eating disorder characterized by restrictive eating and an intense fear of gaining weight, which can lead to physical and psychological health complications.</br></br>
        <b>Antisocial Personality Disorder (ASPD), Psychopathy, and Conduct Disorder </b>
        </br>A group of mental health conditions characterized by disregard for the rights and feelings of others, impulsivity, and a lack of empathy or remorse.</br></br>
        <b>Anxiety </b>
        </br> A condition characterized by excessive worry, fear, or nervousness, which can impact daily functioning and overall well-being.</br></br>
        <b>Asperger Syndrome </b>
        </br>A developmental disorder characterized by difficulties with social communication, restricted interests, and repetitive behaviors.</br></br>
        <b>Attachment Problems </b>
        </br>A condition where an individual has difficulty forming healthy, secure attachments with others, which can impact emotional regulation and social functioning.</br></br>
        <b>Attention Deficit Disorders (ADD/ADHD) </b>
        </br> A condition characterized by difficulty with attention, hyperactivity, and impulsivity, which can impact academic, occupational, and social functioning.</br></br>
        <b>Auditory Processing Disorder (APD) </b>
        </br>A condition where the brain has difficulty processing and interpreting auditory information, which can impact language development, communication, and social functioning.</br></br>
        <b>Autism and Autism Spectrum Disorders </b>
        </br> A group of developmental disorders characterized by difficulties with social communication, restricted interests, and repetitive behaviors, which can impact emotional regulation, social functioning, and daily living skills.</br></br></p>

        <h1><center> B </center></h1>
        <p class="health"><b>Behaviour and Conduct Problems</b>
        </br>This term refers to a range of behavioral and emotional problems that typically develop in childhood or adolescence, such as aggressive or destructive behavior, defiance, impulsivity, and difficulty controlling one's emotions. These problems can interfere with a child's ability to function in school, at home, and in social situations.</br></br>
        <b>Bipolar Disorders</b>
        </br>Bipolar disorder, also known as manic-depressive illness, is a mood disorder characterized by extreme shifts in mood, energy, and activity levels. People with bipolar disorder may experience episodes of mania (periods of intense energy, euphoria, and impulsive behavior) and depression (periods of intense sadness, hopelessness, and lethargy). These mood episodes can last for days, weeks, or even months.</br></br>
        <b>Borderline Personality Disorder (BPD)</b>
        </br>BPD is a mental health condition characterized by intense and unstable emotions, impulsive behavior, and unstable relationships with others. People with BPD may experience intense fear of abandonment, have a distorted self-image, and engage in self-harming behaviors. This condition is typically diagnosed in early adulthood and can be difficult to treat.</br></br>
        <b>Bulimia</b>
        </br>Bulimia is an eating disorder characterized by binge eating (consuming large amounts of food in a short period) and purging (using methods like vomiting, laxatives, or excessive exercise to compensate for the binge). People with bulimia often feel a loss of control during binge episodes and may experience feelings of shame or guilt afterward.</br></br>
        <b>Bullying</b>
        </br>Bullying is a form of aggressive behavior that involves intentionally harming or intimidating someone who is perceived as vulnerable or weaker than oneself. Bullying can take many forms, including physical, verbal, and social bullying. It can have serious consequences for the victim, including emotional and psychological harm.</br></p>

